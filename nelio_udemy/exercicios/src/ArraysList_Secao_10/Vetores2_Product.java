package ArraysList_Secao_10;

public class Vetores2_Product {
  private String name;
  private double price;

  public Vetores2_Product(String name, double price){
    this.name = name;
    this.price = price;
  }

  public String getName(){
    return this.name;
  }

  public void setName(String name){
    this.name = name;
  }

  public double getPrice(){
    return this.price;
  }

  public void setPrice(double value){
    this.price = value;
  }
}
